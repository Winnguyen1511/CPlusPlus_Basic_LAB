#include <iostream>

using namespace std;

int main()
{
    cout << "A int is stored in " << sizeof (int) << " bytes\n";
    cout << "a char is stored in "<<sizeof(char) << " bytes\n";
    cout << "a float is stored in "<<sizeof(float) <<" bytes\n";
    cout << "a double is stored in " <<sizeof(double) <<" bytes\n";
}
